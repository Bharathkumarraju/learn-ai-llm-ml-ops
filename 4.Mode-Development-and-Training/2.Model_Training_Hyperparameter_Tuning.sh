Steps involving in training a machine-learning model and optimizing its performance...


Once model is ready need to measure the metrics -
  1. Precision - Correctly Predicted Positive Instances/Total Predicted Positives
  2. Recall - Correctly Predicted Positive Instances/Total Actual Positives


Model Traning with a larger dataset - Hyperparameter Tuning for iterative model improvement.


1. Train Model with the larger dataset.
2. Evaluate Precision and Recall at each stage.
3. Training data partitioning - Split -- 70% Training, 15% Validation, 15% Testing.
4. Hyperparameter Optimization(Grid Search)
5. Cross-Validation(5-Fold Cross Validation)
6. Performance Evaluation(85% Accuracy - F1-Score:0.80)

