ML:
--->
ML is about creating predictive models by finding patterns in data.
This process involves two main phases - Training and Inferencing

1. Training Data - Historical data
   1. Labels and Features
   2. Algorithm
   3. Model

2. Inferencing
  1. Train the Model
  2. Prediction

ML helps us to make informed decisions about the future.


ML types:
-------------->
1. Supervised Machine Learning --> Training data includes known labels.
      1. Regression - Label is a numeric value
      2. Classification(Label is a Categorization) - Binary Classification, Multiclass Classification

2. Unsupervised Machine Learning --> Training data in unlabeled.

