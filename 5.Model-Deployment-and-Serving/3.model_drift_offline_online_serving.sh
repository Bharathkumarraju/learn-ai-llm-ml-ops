When the model is ready in production , it is needed to be updated with new learning everytime.

Consistently monitoring and retraing the model is necessary for the accurate prediction on the latest data.


Online Serving:
------------------->
Whenever we get the new data(new feature) these data are getting updated to the feature-store, so based on this new input, Machine-Learning model continuously gets trained,This is called an online-prediction

In the offline serving the new data getting updated to the feature store, but the machine learning model itself is getting trained in Batch Manner.

