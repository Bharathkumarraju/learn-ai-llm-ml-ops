### 1. **Absorption of Photons**
When sunlight reaches the solar panel, it is composed of particles known as photons. Each photon carries a specific amount of energy that depends on its wavelength. Photovoltaic cells are typically made from semiconductor materials, most commonly silicon. When photons hit the surface of the solar cell, their energy is absorbed by the semiconductor.

### 2. **Excitation of Electrons**
The energy absorbed from the photon excites the electrons in the silicon atoms, giving them enough energy to break free from their atomic bonds. This process creates electron-hole pairs. The 'hole' is simply the absence of an electron in the atomic structure of the silicon, leading to a positively charged vacancy.

### 3. **Formation of the Electric Field**
In a typical photovoltaic cell, there are two layers of silicon that are treated to create a p-n junction:
- The **p-type silicon layer** is doped with elements (like boron) that create holes, making it rich in positive charge carriers (holes).
- The **n-type silicon layer** is doped with elements (like phosphorus) that provide extra electrons, making it rich in negative charge carriers (electrons).

When these two layers are placed together, an electric field is formed at the junction between the two layers. This electric field plays a crucial role in the movement of the charge carriers.

### 4. **Separation of Charge Carriers**
The electric field at the p-n junction drives the free electrons (created by photon absorption) toward the n-type layer and the holes toward the p-type layer. This movement separates the positive and negative charges: electrons accumulate in the n-type layer while holes accumulate in the p-type layer.

